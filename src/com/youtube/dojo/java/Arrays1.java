package com.youtube.dojo.java;

public class Arrays1 {
	public static void main(String[] args) {
		
		//criar array com 3 posicoes
		int[] idade = new int [3];
		
		//Preencher arrays
		idade[0] = 20;
		idade[1] = 15;
		idade[2] = 30;
		
		//Buscar valores
		System.out.println("Idade1 = " +idade[0]);
		System.out.println("Idade2 = " +idade[1]);
		System.out.println("Idade3 = " +idade[2]);
	}

}
